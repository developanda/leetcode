-- ============================================================
-- [심화] 위 CTE 없이 REGEXP 한 줄로 표현하는 최종 제출용 쿼리
-- ============================================================
-- 분석 관점:
--   실제 LeetCode 제출 시에는 아래처럼 한 줄 REGEXP 로도 충분하다.
--   단, 패턴 해석이 어렵기 때문에 위 단계별 CTE 를 먼저 검토 후 사용.
--
--   패턴 해설:
--     ^[A-Za-z0-9_]+   → 로컬 파트 : 문자열 시작부터 영숫자·언더스코어 1자 이상
--     @                → @ 기호 (REGEXP 특성상 @ 1개 조건 자동 충족)
--     [A-Za-z]+        → 도메인 파트 : 영문자 1자 이상
--     \\.com$          → 문자열이 정확히 .com 으로 종료
--                         (MySQL REGEXP 에서 점 이스케이프 = \\.)
-- ============================================================

SELECT user_id, email
FROM Users
WHERE email REGEXP '^[A-Za-z0-9_]+@[A-Za-z]+\\.com$'
ORDER BY user_id ASC;